// ── Helper to generate realistic price history
function generatePriceHistory(currentPrice, days) {
  const history = [];
  const today = new Date();
  let price = currentPrice;
  for (let i = days; i >= 0; i--) {
    const date = new Date(today);
    date.setDate(today.getDate() - i);
    // Random walk ±8%
    const change = (Math.random() - 0.5) * 0.12 * price;
    price = Math.max(currentPrice * 0.7, Math.min(currentPrice * 1.4, price + change));
    history.push({
      date: date.toISOString().split('T')[0],
      price: Math.round(price * 10) / 10,
      mandiPrice: Math.round(price * 1.35 * 10) / 10,
    });
  }
  history[history.length - 1].price = currentPrice;
  return history;
}

